    {
        $books = Book::select('id', 'genre_id', 'name', 'author', 'description', 'book_cover',)
        ->with('genre:id,name', 'tags:id,label,color')
        ->paginate(10);

        foreach ($books as $book) {
            $book->description = $book->getDescriptionIndeX(200);
            $book->cover_img = $book->getAbsUriImage();
        }

        return response()->json($books);
    }

